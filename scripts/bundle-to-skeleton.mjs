#!/usr/bin/env node
// bundle-to-skeleton.mjs — mechanical recording-bundle → skeleton .md converter.
// See README/CLAUDE.md; no LLM, no narration parsing, no intent inference.
import { readFile, writeFile } from 'node:fs/promises';
import { basename, dirname, resolve } from 'node:path';
import { unzipSync, strFromU8 } from 'fflate';

const HEADER = '<!-- MECHANICAL SKELETON generated by bundle-to-skeleton.mjs. Replace section headings with real intent; narration is in audio.webm. -->';
const USAGE = `usage: bundle-to-skeleton.mjs <bundle.zip> [-o out.md]`;

function die(msg, code = 1) { process.stderr.write(`bundle-to-skeleton: ${msg}\n`); process.exit(code); }
function warn(msg) { process.stderr.write(`bundle-to-skeleton: warning: ${msg}\n`); }

function parseArgs(argv) {
  const args = { input: null, output: null };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--help' || a === '-h') return { help: true };
    else if (a === '-o' || a === '--output') args.output = argv[++i];
    else if (!args.input) args.input = a;
    else die(`unexpected argument: ${a}`);
  }
  return args;
}

function yamlString(s) {
  if (s == null) return '""';
  const str = String(s);
  if (/^[A-Za-z0-9 _\-./:]+$/.test(str) && !/^\s|\s$/.test(str)) return str;
  return JSON.stringify(str);
}
function yamlInline(v) {
  if (v == null) return 'null';
  if (typeof v === 'number' || typeof v === 'boolean') return String(v);
  return yamlString(v);
}
function readJson(files, name) {
  const f = files[name];
  if (!f) return null;
  try { return JSON.parse(strFromU8(f)); }
  catch (e) { die(`failed to parse ${name}: ${e.message}`); }
}

function locatorFor(ev) {
  const locs = ev?.target?.locators;
  if (Array.isArray(locs) && locs.length > 0) return { primary: locs[0], fallbacks: locs.slice(1) };
  const css = ev?.target?.css;
  return { primary: css || null, fallbacks: [], noStable: true };
}

function emitLocatorHeader(lines, loc, indent = '  ') {
  if (loc.noStable && !loc.primary) lines.push(`${indent}# no stable locator captured — manual selection needed`);
  else if (loc.noStable) lines.push(`${indent}# no stable locator captured — manual selection needed (fell back to css)`);
  for (const fb of loc.fallbacks) lines.push(`${indent}# fallback: ${fb}`);
}
function screenshotLine(ev, indent = '  ') {
  if (ev.screenshot_id == null) return null;
  return `${indent}- screenshot: step-${ev.screenshot_id}.png`;
}

function renderEvent(ev, block) {
  const k = ev.kind;
  if (k === 'focus' || k === 'scroll') return;
  if (k === 'idle') {
    const d = Number(ev.duration_ms || 0);
    if (d < 2000) return;
    const rounded = Math.round(d / 100) * 100;
    block.push(`  - wait_for: {timeout_ms: ${rounded}}`);
    return;
  }
  if (k === 'submit') { block.push(`  # submit event; relying on prior click/key`); return; }
  if (k === 'tab_switch') { block.push(`  # tab switch to ${ev.toUrl || ev.url || '<unknown>'} — manual adjustment needed`); return; }
  if (k === 'navigation') {
    const url = ev.to ?? ev.url ?? null;
    block.push(`  - goto: ${yamlInline(url)}`);
    const s = screenshotLine(ev); if (s) block.push(s);
    return;
  }
  if (k === 'click' || k === 'dblclick') {
    const loc = locatorFor(ev);
    emitLocatorHeader(block, loc);
    block.push(`  - click: ${yamlInline(loc.primary ?? '')}`);
    const s = screenshotLine(ev); if (s) block.push(s);
    return;
  }
  if (k === 'input' || k === 'change') {
    const loc = locatorFor(ev);
    emitLocatorHeader(block, loc);
    if (ev.is_masked === true) {
      const n = ev.value_length ?? (ev.value ? String(ev.value).length : 0);
      block.push(`  # MASKED (length=${n}): set value explicitly`);
      block.push(`  - fill:`);
      block.push(`      selector: ${yamlInline(loc.primary ?? '')}`);
      block.push(`      value: \${SECRET}`);
    } else {
      block.push(`  - fill:`);
      block.push(`      selector: ${yamlInline(loc.primary ?? '')}`);
      block.push(`      value: ${yamlInline(ev.value ?? '')}`);
    }
    const s = screenshotLine(ev); if (s) block.push(s);
    return;
  }
  if (k === 'key') {
    const loc = locatorFor(ev);
    if (loc.primary) emitLocatorHeader(block, loc);
    block.push(`  - press:`);
    block.push(`      selector: ${loc.primary ? yamlInline(loc.primary) : 'null'}`);
    block.push(`      key: ${yamlInline(ev.key ?? '')}`);
    const s = screenshotLine(ev); if (s) block.push(s);
    return;
  }
  block.push(`  # unknown event kind: ${k}`);
}

function buildDocument(manifest, events) {
  const label = manifest.label || 'Recorded session';
  const description = manifest.description ?? null;
  const startUrl = manifest.start_url || '';
  const lines = [];
  lines.push(HEADER);
  lines.push('---');
  lines.push(`title: ${yamlString(label)}`);
  lines.push(`description: ${yamlString(description ?? 'Generated by bundle-to-skeleton')}`);
  lines.push(`runtime: browser`);
  lines.push('---');
  lines.push('');
  if (description) { lines.push(description); lines.push(''); }
  lines.push(`# ${label}`);
  lines.push('');

  if (!events || events.length === 0) {
    lines.push('<!-- no events in bundle — add verbs manually below -->');
    lines.push('```browser');
    lines.push('verbs:');
    if (startUrl) lines.push(`  - goto: ${yamlInline(startUrl)}`);
    lines.push('```');
    lines.push('');
    return lines.join('\n');
  }

  let block = null;
  const openBlock = () => { block = ['```browser', 'verbs:']; };
  const closeBlock = () => { if (block) { lines.push(...block, '```', ''); block = null; } };
  const pendingNotes = [];
  const flushNotes = () => { for (const n of pendingNotes) lines.push(`> operator note: ${n}`); pendingNotes.length = 0; };

  openBlock();
  if (startUrl) block.push(`  - goto: ${yamlInline(startUrl)}`);

  // Dedupe the bootstrapping goto if the first navigation targets the same URL.
  const firstNavIdx = events.findIndex(e => e && e.kind === 'navigation');
  const firstMeaningfulIdx = events.findIndex(e => e && e.kind !== 'marker' && e.kind !== 'note' && e.kind !== 'idle');
  let skipFirstNav = false;
  if (startUrl && firstNavIdx !== -1 && firstNavIdx === firstMeaningfulIdx) {
    const navTo = events[firstNavIdx].to ?? events[firstNavIdx].url ?? null;
    if (navTo === startUrl) skipFirstNav = true;
  }

  for (let i = 0; i < events.length; i++) {
    const ev = events[i];
    if (skipFirstNav && i === firstNavIdx) { skipFirstNav = false; continue; }
    if (ev.kind === 'marker') {
      closeBlock();
      flushNotes();
      lines.push(`## ${ev.label || 'Section'}`);
      lines.push('');
      openBlock();
      continue;
    }
    if (ev.kind === 'note') {
      const text = ev.text || ev.note || '';
      if (text) pendingNotes.push(text);
      continue;
    }
    if (pendingNotes.length) {
      const saved = block; block = null;
      flushNotes();
      lines.push('');
      block = saved;
    }
    renderEvent(ev, block);
  } // end for
  closeBlock();
  flushNotes();
  return lines.join('\n').replace(/\n{3,}/g, '\n\n');
}

async function main() {
  const argv = process.argv.slice(2);
  if (argv.length === 0) { process.stderr.write(USAGE + '\n'); process.exit(2); }
  const args = parseArgs(argv);
  if (args.help) { process.stdout.write(USAGE + '\n'); return; }
  if (!args.input) die(USAGE, 2);

  const inputPath = resolve(args.input);
  let buf;
  try { buf = await readFile(inputPath); } catch (e) { die(`cannot read ${inputPath}: ${e.message}`); }
  let files;
  try { files = unzipSync(new Uint8Array(buf)); }
  catch { die(`${inputPath} is not a zip`); }

  const manifest = readJson(files, 'manifest.json');
  if (!manifest) die(`${inputPath} is missing manifest.json`);
  const fmt = manifest.format || '';
  const m = /^recaptain-recording\/(\d+(?:\.\d+)?)$/.exec(fmt);
  if (!m || parseFloat(m[1]) < 2) warn(`unexpected bundle format "${fmt}" (expected recaptain-recording/2+); continuing`);

  const events = readJson(files, 'events.json') || [];
  const md = buildDocument(manifest, events);

  const outPath = args.output
    ? resolve(args.output)
    : resolve(dirname(inputPath), basename(inputPath).replace(/\.zip$/, '') + '.skeleton.md');
  await writeFile(outPath, md.endsWith('\n') ? md : md + '\n');
  process.stderr.write(`bundle-to-skeleton: wrote ${outPath} (${events.length} events, ${md.split('\n').length} lines)\n`);
}

main().catch(e => die(e?.stack || String(e)));
